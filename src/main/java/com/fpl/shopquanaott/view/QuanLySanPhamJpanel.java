    public void loadTable(List<SPCT> list){
        DefaultTableModel dtm = (DefaultTableModel) tblBangSP.getModel();
        dtm.setRowCount(0);
        for (SPCT sp : list) {
            dtm.addRow(new Object[]{
              sp.getMaSP(),
              tenRP.layTen(sp.getTenSP()),
      //        lRP.layTen(sp.getLoaiSP()),
              sRP.layTen(sp.getSizeSP()),
              msRP.layTen(sp.getMauSac()),
       //       clRP.layTen(sp.getChatLieu()),
              sp.getSlTon(),
              sp.getGiaNhap(),
              sp.getGiaBan(),
              sp.getTinhTrang() == 1 ? "Còn bán" : "Ngừng bán"
            
            });
        }
    }
    
    public void loadTableTenSP(){
     DefaultTableModel dtm = (DefaultTableModel) tblBangLoai.getModel();
        dtm.setRowCount(0);
        List<SanPham> list = tenRP.getAll();
        for (SanPham s : list) {
            dtm.addRow(new Object[]{
              s.getMaTen(),
              s.getTenSP(),
              spSer.laySLSP(s.getMaTen())
            });
        }
    
    }
    
    public void loadCBBLoai(){
        DefaultComboBoxModel dcb = (DefaultComboBoxModel) cbbLoai.getModel();
        dcb.removeAllElements();
        List<LoaiSanPham> listLSP = lRP.getAll();
        for (LoaiSanPham l : listLSP) {
            dcb.addElement(l.getTen());
        }
    
    }
    
