    public boolean checkData(){
    if(txtMaSP.getText().trim().isEmpty() || txtSL.getText().trim().isEmpty() 
       || txtGiaGoc.getText().trim().isEmpty()){
        JOptionPane.showMessageDialog(this,"Không được để trống dữ liệu");
        return true;
     }
    
    boolean checkGiaNhap = false;
    boolean checkGiaBan = false;
    boolean checkSL = false;
    checkGiaNhap = txtGiaGoc.getText().matches("^[1-9][0-9]*(\\.[0-9]+)?$");
    checkGiaBan = txtGiaGoc.getText().matches("^[1-9][0-9]*(\\.[0-9]+)?$");
    checkSL = txtSL.getText().matches("^[1-9][0-9]*$");
        
    if(checkSL == false){
     JOptionPane.showMessageDialog(this,"Sai số lượng");
         return true;
    }
    if(checkGiaNhap == false || checkGiaBan == false){
     JOptionPane.showMessageDialog(this,"Sai giá");
         return true;
    }
        
    return false;
    }
    
    
